package inheritance;

public class Theory
{
	/*
	 * Inheritance ----> The process by which one class acquire the properties & functionality 
	 *                  of another class is Inheritance. The aim of inheritance is to provide reusability
	 *                  of code.
	 * 
	 * Imp terminology
	 * 
	 * Superclass --> The class whose features are inherited[ base class, parent class]
	 * 
	 * Subclass --> The class that inherits the other class [derived class,child class ,extended class ]
	 * subclass can add its own method and fields in addition to superclass.
	 * 
	 * Reusability ---> Inheritance supports the concept of reusability
	 * 
	 * Types of Inheritance 
	 * 
	 * Single Inheritance -->Subclass inherits the feature of one superclass
	 * 
	 * Multilevel Inheritance--> A subclass will be inheriting a superclass & as well as subclass will 
	 * also act as superclass  to other class
	 * 
	 * Hierarchical Inheritance --->One Class serves as superclass for more than one subclass
	 * 
	 * Multiple Inheritance --->One class can have more than one super class & inherits features from
	 * all the class.Java does not support MI with classes,can be achieve with interfaces.
	 * 
	 * Hybrid Inheritance ---->It is mix of two or more above type of inheritance.Java
	 * does not support MI with classes ,can be achieved with Interfaces
	 * 
	 *                  
	 *                  
	 */

	

}
